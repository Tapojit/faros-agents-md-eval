#!/usr/bin/env bash
# install_deps.sh - Install CPython build dependencies across macOS, Linux, and Cowork.
#
# Usage: bash scripts/install_deps.sh [workspace_dir]
#
# Environment detection order:
#   1. macOS → brew install
#   2. Linux with headers present → skip (already installed, e.g. via Cowork setup script)
#   3. Linux with sudo → apt-get install
#   4. Linux without sudo (Cowork sandbox) → apt-get download + dpkg-deb extract to $HOME/devlibs
#
# Outputs:
#   - System deps installed (system-wide, Homebrew, or user-local)
#   - workspace/build_env.sh created if non-standard paths are needed
#   - Exit 0 on success, 1 on failure

set -uo pipefail

WORKSPACE="${1:-workspace}"
BUILD_ENV="$WORKSPACE/build_env.sh"
DEVPREFIX="$HOME/devlibs"

# Required headers to verify installation
REQUIRED_HEADERS="openssl/ssl.h readline/readline.h sqlite3.h bzlib.h ncurses.h lzma.h"

check_headers_at() {
    local prefix="$1"
    for h in $REQUIRED_HEADERS; do
        [ ! -f "$prefix/include/$h" ] && return 1
    done
    return 0
}

verify_and_report() {
    local prefix="$1"
    local all_ok=true
    for h in $REQUIRED_HEADERS; do
        if [ -f "$prefix/include/$h" ]; then
            echo "[deps]   OK: $h"
        else
            echo "[deps]   MISSING: $h" >&2
            all_ok=false
        fi
    done
    $all_ok
}

# ──────────────────────────────────────────────
# macOS
# ──────────────────────────────────────────────
if [ "$(uname -s)" = "Darwin" ]; then
    echo "[deps] Detected macOS"

    if ! command -v brew &>/dev/null; then
        echo "[deps] ERROR: Homebrew not found. Install from https://brew.sh" >&2
        exit 1
    fi

    BREW_PREFIX=$(brew --prefix)
    BREW_PKGS="openssl readline sqlite3 xz zlib"

    echo "[deps] Installing via Homebrew: $BREW_PKGS"
    brew install $BREW_PKGS 2>/dev/null || brew upgrade $BREW_PKGS 2>/dev/null || true

    # Verify openssl header exists
    if [ ! -f "$BREW_PREFIX/opt/openssl/include/openssl/ssl.h" ]; then
        echo "[deps] ERROR: openssl headers not found after brew install." >&2
        echo "[deps] Try: brew reinstall openssl" >&2
        exit 1
    fi

    # Create build_env.sh for macOS (Homebrew paths aren't in default search path)
    mkdir -p "$WORKSPACE"
    cat > "$BUILD_ENV" <<ENVEOF
# Auto-generated by install_deps.sh (macOS/Homebrew)
BREW_PREFIX=\$(brew --prefix)
export CPPFLAGS="-I\$BREW_PREFIX/opt/openssl/include -I\$BREW_PREFIX/opt/readline/include -I\$BREW_PREFIX/opt/sqlite/include -I\$BREW_PREFIX/opt/xz/include -I\$BREW_PREFIX/opt/zlib/include"
export LDFLAGS="-L\$BREW_PREFIX/opt/openssl/lib -L\$BREW_PREFIX/opt/readline/lib -L\$BREW_PREFIX/opt/sqlite/lib -L\$BREW_PREFIX/opt/xz/lib -L\$BREW_PREFIX/opt/zlib/lib"
export CPYTHON_CONFIGURE_EXTRA="--with-openssl=\$BREW_PREFIX/opt/openssl"
ENVEOF

    echo "[deps] macOS deps installed via Homebrew."
    echo "[deps] Build environment saved to $BUILD_ENV"
    exit 0
fi

# ──────────────────────────────────────────────
# Linux: Check if headers already exist system-wide
# ──────────────────────────────────────────────
echo "[deps] Detected Linux ($(uname -m))"
echo "[deps] Checking system headers..."

if check_headers_at /usr; then
    echo "[deps] All required headers found in /usr/include. No installation needed."
    # No build_env.sh needed — standard paths work
    if ! command -v bc &>/dev/null; then
        echo "[deps] WARNING: 'bc' not found. record_metrics.sh needs it for cost calculations."
    fi
    exit 0
fi

# ──────────────────────────────────────────────
# Linux: Try sudo apt-get
# ──────────────────────────────────────────────
REQUIRED_PKGS="build-essential libffi-dev libssl-dev zlib1g-dev libreadline-dev libsqlite3-dev libbz2-dev libncurses-dev liblzma-dev bc"

echo "[deps] Headers missing. Trying sudo apt-get install..."
if sudo -n true 2>/dev/null && sudo apt-get update -qq 2>/dev/null && sudo apt-get install -y -qq $REQUIRED_PKGS 2>/dev/null; then
    echo "[deps] System packages installed successfully via apt-get."
    # No build_env.sh needed — standard paths work
    exit 0
fi

# ──────────────────────────────────────────────
# Linux without sudo: Download .deb and extract to user prefix
# ──────────────────────────────────────────────
echo "[deps] sudo not available (sandbox or container). Falling back to user-local prefix."
echo "[deps] Target: $DEVPREFIX"

DOWNLOAD_PKGS="libssl-dev libreadline-dev libsqlite3-dev libbz2-dev libncurses-dev liblzma-dev"

mkdir -p "$DEVPREFIX/include" "$DEVPREFIX/lib"

TMPDIR=$(mktemp -d)
trap 'rm -rf "$TMPDIR"' EXIT

cd "$TMPDIR"
echo "[deps] Downloading .deb packages..."
if ! apt-get download $DOWNLOAD_PKGS 2>&1; then
    echo "[deps] ERROR: apt-get download failed." >&2
    echo "[deps] If network is restricted, use Cowork's Setup Script to pre-install deps." >&2
    exit 1
fi

echo "[deps] Extracting headers and libraries..."
mkdir -p "$TMPDIR/extracted"
for deb in *.deb; do
    dpkg-deb -x "$deb" "$TMPDIR/extracted/" 2>/dev/null
done

# Copy headers
if [ -d "$TMPDIR/extracted/usr/include" ]; then
    cp -r "$TMPDIR/extracted/usr/include/"* "$DEVPREFIX/include/"
fi

# Detect architecture for lib paths
ARCH=$(dpkg --print-architecture 2>/dev/null || echo "amd64")
case "$ARCH" in
    arm64) LIBARCH="aarch64-linux-gnu" ;;
    amd64) LIBARCH="x86_64-linux-gnu" ;;
    *)     LIBARCH="$ARCH-linux-gnu" ;;
esac

# Copy static libs and symlinks
if [ -d "$TMPDIR/extracted/usr/lib/$LIBARCH" ]; then
    cp -r "$TMPDIR/extracted/usr/lib/$LIBARCH/"* "$DEVPREFIX/lib/"
fi

# CRITICAL: Merge arch-specific OpenSSL headers.
#
# Debian's libssl-dev puts opensslconf.h (and opensslv.h on some versions) in
# /usr/include/<arch>/openssl/ rather than /usr/include/openssl/. When we extract
# to a custom prefix, CPython's configure checks $PREFIX/include/openssl/ssl.h which
# includes <openssl/opensslconf.h> — but that file is one directory deeper in the
# arch-specific tree. Without this merge step:
#   - configure silently marks _ssl as "missing"
#   - The build succeeds (no error)
#   - `import ssl` fails at runtime
#   - The functional-test grader fails
# This is the #1 gotcha when building CPython with extracted .deb headers.
ARCH_INCLUDE="$DEVPREFIX/include/$LIBARCH"
if [ -d "$ARCH_INCLUDE/openssl" ]; then
    echo "[deps] Merging arch-specific OpenSSL headers ($LIBARCH → openssl/)..."
    cp "$ARCH_INCLUDE/openssl/"* "$DEVPREFIX/include/openssl/"
fi

# Symlink system shared libs so the linker finds them at build time
echo "[deps] Symlinking system shared libraries..."
for lib in /lib/$LIBARCH/lib{ssl,crypto,sqlite3,readline,ncurses,ncursesw,bz2,lzma,ffi}.so*; do
    [ -f "$lib" ] || continue
    bn=$(basename "$lib")
    [ ! -e "$DEVPREFIX/lib/$bn" ] && ln -sf "$lib" "$DEVPREFIX/lib/$bn" 2>/dev/null
done

# Verify
echo "[deps] Verifying installation..."
if ! verify_and_report "$DEVPREFIX"; then
    echo "[deps] ERROR: Some headers still missing after extraction." >&2
    echo "[deps] Best fix: add apt-get install to Cowork's Setup Script (runs before sandbox)." >&2
    exit 1
fi

# Create build_env.sh
mkdir -p "$WORKSPACE"
cat > "$BUILD_ENV" <<ENVEOF
# Auto-generated by install_deps.sh (Linux user-local prefix)
# Source this before configure/make when system deps are in \$HOME/devlibs
export DEVPREFIX="$DEVPREFIX"
export CPPFLAGS="-I\$DEVPREFIX/include"
export LDFLAGS="-L\$DEVPREFIX/lib -L/lib/$LIBARCH"
export LD_LIBRARY_PATH="\$DEVPREFIX/lib:\${LD_LIBRARY_PATH:-}"
export CPYTHON_CONFIGURE_EXTRA="--with-openssl=\$DEVPREFIX"
ENVEOF

echo "[deps] User-local prefix ready at $DEVPREFIX"
echo "[deps] Build environment saved to $BUILD_ENV"

# Check bc (needed by record_metrics.sh)
if ! command -v bc &>/dev/null; then
    echo "[deps] WARNING: 'bc' not found. record_metrics.sh needs it for cost calculations."
    echo "[deps] Add 'bc' to Cowork Setup Script, or computations will fail."
fi

exit 0
